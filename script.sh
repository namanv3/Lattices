#!/bin/bash

curr=("1 3 4 5 2")
echo Order of execution of the directories is: $curr
echo >> temp$1.txt
for i in $curr
do
    echo ------------------------------------------------------------------------------------------------------------------------------------
    cd $i
    # make clean
    make
    ./main $1 $1 >> ../temp$1.txt
    echo >> ../temp$1.txt
    cd ..
done

echo ------------------------------------------------------------------------------------------------------------------------------------
grep '^Time' temp$1.txt > $1.txt
echo >> $1.txt
grep '^The smallest' temp$1.txt >> $1.txt
echo >> $1.txt
grep '^The largest' temp$1.txt >> $1.txt

echo ------------------------------------------------------------------------------------------------------------------------------------
echo ------------------------------------------------------------------------------------------------------------------------------------
echo ------------------------------------------------------------------------------------------------------------------------------------
